////////////////////////////////////////////////////////////////////////////////////////////
//Q3.Write a program to print 5 to 1 numbers on console.
////////////////////////////////////////////////////////////////////////////////////////////
#include<stdio.h>
int Display()
{
    int i=5;
    while(i>0)
    {
        printf("%d\n",i);
        i--;
    }
}
int main()
{
    Display();
    return 0;   
}

/*

output:
5
4
3
2
1

*/