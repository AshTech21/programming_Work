////////////////////////////////////////////////////////////////////////////////////////////
//Q2.Write a program to print 5 times "Marvellous" on console.
////////////////////////////////////////////////////////////////////////////////////////////
#include<stdio.h>
void Display()
{
    int i=0;
    for(i=0;i<=5;i++)
    {
        printf("Marvellous\n");
    }
}
int main()
{
    Display();
    return 0;
}

