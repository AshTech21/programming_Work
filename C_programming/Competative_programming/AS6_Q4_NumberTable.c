#include <stdio.h>
void Table(int iNo)
{
  int iCnt = 0;
  for(iCnt =1; iCnt<=10 ; iCnt ++)
  {
    printf("%d\t",iNo * iCnt);
  }
  printf("\n");
}
int main()
{
    int iValue = 0;
    int iRet = 0;

    printf("Enter a Number: ");
    scanf("%d", &iValue);
    Table(iValue);
    return 0;
}

