//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// Q3.Write a program which accepts distance in kilometers from user and convert it into meters.
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
#include <stdio.h>
int KMtoMeter(int iNo)
{
  return iNo * 1000;
}
int main()
{
  int iValue = 0;
  int iRet = 0.0;
  printf("Enter Distance : ");
  scanf("%d",&iValue);
  
  iRet = KMtoMeter(iValue);
  printf("Distance in Meters is : %d m \n",iRet);
  return 0;
}

/*

output:

Enter Distance : 12
Distance in Meters is : 12000 m 

*/